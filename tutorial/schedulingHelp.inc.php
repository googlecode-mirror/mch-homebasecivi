<p> <strong>How to Manage the Master Schedule</strong></p>

<p>The Master Schedule has three parts parts: one for the <b>Food Bank</b>, <b>Soup Kitchen</b> and <b>Food Pantry</b>.</p>

<p>The correct master schedule should be fetched for you when you load the page. If it is not, select the right one by choosing from the options:</p>

<img src="tutorial/screenshots/changeschedule.png" width="400px" border="1px" align="middle"></a>

<p>The examples below show how to work with the <b>Food Bank</b> master schedule.  These same steps can be followed when working with any other master schedule. The <b>Food Bank</b> master schedule can be seen below. </p></br></br>

<img src="tutorial/screenshots/masterschedule.png" width="400px" border="1px" align="middle"></a>

<p> The schedule is organized by weeks. Working down the calendar, each row corresponds to a week of the month. </p>

<p>A "shift" is shown as a box associated with a particular day (e.g., Thursday) If a shift has vacancies, the number of vacancies is shown in <strong>boldface</strong>. </p>

<img src="tutorial/screenshots/vacancies.png" width="400px" border="1px" align="middle"></a>

<p> <B>Step 2:</B> If you want to fill a vacancy for a shift, you must first select it. </p></br>

<p>For example, suppose you want to add a person to fill the vacancy on the <strong>Monday</strong> Week 1 crew.  To do this, you first select that shift on the schedule. </p></br>


<p> <B>Step 3:</B> Now you will see a crew worksheet for the <strong>Monday</strong> Week 1 shift.</p></br>

<img src="tutorial/screenshots/crewsheet.png" width="400px" border="1px" align="middle"></a>

<p>You can find out who's available for that crew by selecting <strong>Assign Volunteer</strong>, like this: </p></br>

<img src="tutorial/screenshots/assignvolunteer.png" width="400px" border="1px" align="middle"></a>

<p> <B>Step 4:</B> This will give you a list of all volunteers who have that specific availability, or alternately all volunteers who work for that group.</p>

<p> Select a volunteer to add to the schedule. To confirm this addition, select <strong>Back to Crew</strong> and you should see your changes take effect:</p></br>

<img src="tutorial/screenshots/twopeople.png" width="400px" border="1px" align="middle"></a>

<p>Selecting <strong>Back to Master Schedule</strong> will reflect these changes on the Master Schedule as well. </p>

<img src="tutorial/screenshots/scheduleedited.png" width="400px" border="1px" align="middle"></a>


<p> <B>Step 5:</B> Several additional <strong>Master Schedule</strong> functions are also available.  You can remove an entire crew, you can remove a person to create a vacancy, and add or remove a slot from the shift. </p>
    <ul>
	    <li>To remove an existing shift from the Master Schedule, select <strong>Remove Entire Crew</strong> on the worksheet shown in <B>Step 3</B>.</li>
	    <li>To remove a person from a shift, select <strong>Remove Person/Create Vacancy</strong> on the worksheet shown in <B>Step 3</B>. </li>
	    <li>To add a new slot to a shift, select <strong>Add Slot</strong> on the worksheet shown in <B>Step 3</B>. </li>
	    <li>To remove a vacant slot from a shift, select <strong>Remove Vacant Slot</strong> on the worksheet shown in <B>Step 3</B>. </li>
	</ul>
<p> <B>Step 6:</B> When you finish, you can return to any other function by selecting it on the navigation bar.</p>