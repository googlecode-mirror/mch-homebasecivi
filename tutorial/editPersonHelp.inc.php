<p> <strong>How to Edit a Person's Information in the Database</strong></p>

<p> Editing information in the volunteer database is usually done by the administrator.</p>

<p> 
    <strong>Step 1:</strong> First you need to either <strong>view</strong> or <strong>search</strong> for the person whose information you want to edit. </BR>
    After finding that person, select <strong> edit</strong> beside his/her name.
</p>

<p> <strong>Step 2:</strong> You will now see a page with all of the person's information, like this:</BR></p>

<img src="tutorial/screenshots/editperson.png" width="400px" border="1px" align="middle">

<p> 
    <strong>Step 3:</strong> To change any of that information, just retype or reselect it.  For instance, to change Availability,
		select one of the unchecked (checked) boxes and it will become checked (unchecked).
	<BR>&nbsp&nbsp REMEMBER: No fields marked by <font color="#FF0000">*</font> can be left blank.
</p>

<p> <strong>Step 4:</strong> When you are finished making changes, select <strong>Submit</strong> at the bottom of the page:</BR></p>

<img src="tutorial/screenshots/saveperson.png" width="400px" border="1px" align="middle">

<p> <B>Step 5:</B> If an error was made, <font color=#FF0000>red</font> warnings will tell you what needs to be corrected, like this:</p></BR>

<img src="tutorial/screenshots/savepersonerror.png" width="400px" border="1px" align="middle">

<p><BR>After you have made these corrections, repeat <B>Step 4</B>.</BR></p>

<p><B>Step 6:</B> If you have no errors or omissions, you will see a page telling you the edit was successful, like this:</BR></p>
    
<img src="tutorial/screenshots/savepersonsuccess.png" width="400px" border="1px" align="middle">

<p> <B>Step 7:</B> When you finish, you can return to any other function by selecting it on the navigation bar.</p>